package jumpstatement;

public class a6_starpattern {

	public static void main(String[] args) {

		for (int a=7; a>=1; a--) {
		
		 System.out.print(" ");            // for spacing
		 
		 for(int c=1; c<=a; c++) {        // for star pattern
		 
		 System.out.print("*");
		 }

		 System.out.println();           //for go to next line
	}
	
}
}                                                 