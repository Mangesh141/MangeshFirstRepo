package Loopingstatement;

public class a9 {

	public static void main(String[] args) {

		int b=1;
		                                             // it is opposite to while loop and do while loop
		// 1
		
		do {
			
			System.out.println(b);   // 1
			b=b+1;                   // 2----3----4----------10----11(exit)
		
		}
		while(b<=10);
		
		// 1------2------3----4------------10
		
	}

}
